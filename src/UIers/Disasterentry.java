    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        boolean dataUpdated = false;

        for (Ersreport d : ersreporters.getErsreporters()) {
           
            if (d.getDiaster_id().equals(curreenti)) {
                jTextField1.setText(d.getDiaster_id());
                // Check for empty fields
                String endDateTime = txtedt.getText();
                String startDateTime = txtstdandedt.getText();
                String noChildAffectedText = tctnochildaffec.getText();
                String noPplCriticalCondText = txtnopplincriticalcond.getText();
                String noPplMissingText = txtnopplmiss.getText();
                String propertyDamageText = txtnetpropdmg.getText();
                String noMinorInjuriesText = txtnooffpplwithminorinj.getText();
                String magnitudeText = txtmag.getText();
                String noPplAffectedText = txtnopplaffec.getText();

                if (endDateTime.isEmpty() || startDateTime.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "End date, start date, and description fields cannot be empty", "Warning", JOptionPane.WARNING_MESSAGE);
                    continue; // Skip to the next iteration of the loop
                }

                try {
                    double numberOfChildAffected = Double.parseDouble(noChildAffectedText);
                    double numberOfPplCriticalCondition = Double.parseDouble(noPplCriticalCondText);
                    double numberOfPplMissing = Double.parseDouble(noPplMissingText);
                    double propertyDamage = Double.parseDouble(propertyDamageText);
                    double numberOfMinorInjuries = Double.parseDouble(noMinorInjuriesText);
                    float magnitude = Float.parseFloat(magnitudeText);
                    double numberOfPplAffected = Double.parseDouble(noPplAffectedText);

                    // Set the values
                    d.setEnddatetime(endDateTime);
                    d.setStartdatetime(startDateTime);
                    d.setNumber_ofchildaffected(numberOfChildAffected);
                    d.setNumber_ofpplcriticalcndition(numberOfPplCriticalCondition);
                    d.setNumberpplmisssing(numberOfPplMissing);
                    d.setPropertiy_dmg(propertyDamage);
                    d.setNumber_ofminorinjuries(numberOfMinorInjuries);
                    d.setMagnitude(magnitude);
                    d.setNumber_ofpplaffected(numberOfPplAffected);
                    d.setLogstatus("NA");
                    d.setAssignedtologid("NA");
                    d.setStatus("COMPLETE");
                    d.setSeverity((String) jComboBox1.getSelectedItem());

                    dataUpdated = true;

                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Invalid numerical input", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            }
        }

        if (dataUpdated) {
            JOptionPane.showMessageDialog(null, "Report Submitted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

