    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     int selectedRow = tbldiastetview.getSelectedRow();
    String hh = "";

    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(null, "Please select a row from the table to view details", "Warning", JOptionPane.WARNING_MESSAGE);
    } else {
        DefaultTableModel model = (DefaultTableModel) tbldiastetview.getModel();
        Diastermangdirectory disaster = (Diastermangdirectory) model.getValueAt(selectedRow, 0);
        
        disaster.setIdofers(id);
        disaster.setStatus("COMPLETE");
        hh = disaster.getDisid();
        Ersreport usc = ersreporters.addNewReport();
        usc.setErsid(id);
        usc.setDiaster_id(hh);
        usc.setDisastertype(disaster.getDisname());
        usc.setState(disaster.getState_affec());
        usc.setCity(disaster.getCity_name());
        usc.setStatus("INPROGRESS");

        JOptionPane.showMessageDialog(null, "Assigned Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        
        // Refresh the table with the updated data
        populatetabls();
    }

      
    }//GEN-LAST:event_jButton2ActionPerformed

