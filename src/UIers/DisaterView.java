    public void populatetabls(){
        
        DefaultTableModel dtm = (DefaultTableModel) tbldiastetview.getModel();
                dtm.setRowCount(0);        
for (Diastermangdirectory dis : diaster.getDiaster()) 
{
        if (dis.getDisname().equals(Disaster) && dis.getStatus().equals("INPROGRESS"))
        {
            Object[] row = new Object[4];
            row[0] = dis;
            row[1] = dis.getDisname();
            row[1] = dis.getState_affec();
            row[2] = dis.getCity_name();          
           

            
            dtm.addRow(row);
        }
        }
   }

    
    

