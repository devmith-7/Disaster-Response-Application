    private void btnnnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnnActionPerformed
        // TODO add your handling code here:
         
        int selectedRow = requestJTable2.getSelectedRow();
        if(selectedRow < 0){
              JOptionPane.showMessageDialog(null, "Please select a row from table to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            DefaultTableModel model = (DefaultTableModel) requestJTable2.getModel();
            Requestin disaster = (Requestin) model.getValueAt(selectedRow, 0);
             txtamountraised1.setText(String.valueOf(disaster.getAmountraised()));
             txtamtreq1.setText(String.valueOf(disaster.getAmount()));
             txtstate1.setText(disaster.getStatus());
             txtcustomerid1.setText(disaster.getId());
             txtarearrr.setText(disaster.getDescription());
        }
    
    }//GEN-LAST:event_btnnnActionPerformed

