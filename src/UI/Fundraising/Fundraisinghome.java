    public void  populatetab1(){
         DefaultTableModel dtm = (DefaultTableModel) requestJTable2.getModel();
                dtm.setRowCount(0);        
for (Requestin dis : request.getRequest()) 
{
        if (dis.getStatus().equals("COMPLETE"))
        {
            Object[] row = new Object[4];
            row[0] = dis;
            row[1] = dis.getReason();
            row[2] = dis.getRequestedby();
            row[3] = dis.getDescription();
     
           

            
            dtm.addRow(row);
        }
        }
   }
        
    
        
